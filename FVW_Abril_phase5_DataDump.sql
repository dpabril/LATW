-- IMPORTANT: This script was written for use with PostgreSQL.

DROP TABLE IF EXISTS Admin;
DROP TABLE IF EXISTS Language CASCADE;
DROP TABLE IF EXISTS LanguageFamily CASCADE;
DROP TABLE IF EXISTS Dialect CASCADE;
DROP TABLE IF EXISTS Script CASCADE;
DROP TABLE IF EXISTS Country CASCADE;
DROP TABLE IF EXISTS languageBelongsTo;
DROP TABLE IF EXISTS languageHasDialect;
DROP TABLE IF EXISTS isSpokenIn;
DROP TABLE IF EXISTS languageUsesScript;

-- =======================================================
--                      < Entities >
-- =======================================================

CREATE TABLE Language (
    ISOid VARCHAR(3),
    name VARCHAR(50) NOT NULL,
    population INTEGER NOT NULL,
    PRIMARY KEY (ISOid)
    );

CREATE TABLE LanguageFamily (
    name VARCHAR(50),
    subgroupCount INTEGER NOT NULL,
    info VARCHAR(3000),
    PRIMARY KEY (name)
    );

CREATE TABLE Dialect (
    ISOid VARCHAR(3),
    name VARCHAR(50),
    PRIMARY KEY (ISOid, name),
    FOREIGN KEY (ISOid) REFERENCES Language(ISOid)
        ON UPDATE CASCADE
        ON DELETE CASCADE
    );

CREATE TABLE Script (
    ISOid VARCHAR(4),
    name VARCHAR(50) NOT NULL,
    direction VARCHAR(50) NOT NULL,
    hasCase BOOLEAN NOT NULL,
    PRIMARY KEY (ISOid)
    );

CREATE TABLE Country (
    ISOid VARCHAR(2),
    name VARCHAR(75) NOT NULL,
    offiName VARCHAR(75) NOT NULL,
    population INTEGER NOT NULL,
    PRIMARY KEY (ISOid)
    );

CREATE TABLE Admin (
    username VARCHAR(20),
    password VARCHAR(60),
    name VARCHAR(100) NOT NULL,
    PRIMARY KEY (username)
    );

-- =======================================================
--                    < Relationships >
-- =======================================================

CREATE TABLE languageBelongsTo (
    langID VARCHAR(3),
    lfamID VARCHAR(50) NOT NULL,
    PRIMARY KEY (langID),
    FOREIGN KEY (langID) REFERENCES Language(ISOid)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    FOREIGN KEY (lfamID) REFERENCES LanguageFamily(name)
        ON UPDATE CASCADE
        ON DELETE CASCADE
    );

CREATE TABLE isSpokenIn (
    langID VARCHAR(3),
    countryID VARCHAR(2),
    isNational BOOLEAN,
    PRIMARY KEY (langID, countryID),
    FOREIGN KEY (langID) REFERENCES Language(ISOid)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    FOREIGN KEY (countryID) REFERENCES Country(ISOid)
        ON UPDATE CASCADE
        ON DELETE CASCADE
    );

CREATE TABLE languageUsesScript (
    langID VARCHAR(3),
    scriptID VARCHAR(4),
    PRIMARY KEY (langID, scriptID),
    FOREIGN KEY (langID) REFERENCES Language(ISOid)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    FOREIGN KEY (scriptID) REFERENCES Script(ISOid)
        ON UPDATE CASCADE
        ON DELETE CASCADE
    );


